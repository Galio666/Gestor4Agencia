    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        try {
        if(txtidEmp.getText().equals("")||txtnombreEmp.getText().equals("")||txtVacantes.getText().equals("")||txtDescrip.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la información",JOptionPane.INFORMATION_MESSAGE);
        }
        else{   
           
                DBConexion conexion=new DBConexion();
                Statement stent=conexion.getStatement();
                ResultSet rs=stent.executeQuery("SELECT * FROM EMPLEO ");
                emph=new EmpleoHelper();
                emp=new Empleo(txtidEmp.getText(),txtnombreEmp.getText(), txtDescrip.getText(), txtVacantes.getText());
                if(emph.Agregar(emp)==true){
                    JOptionPane.showMessageDialog(null,"Operacion Exitosa","empleoAgregado",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(true);
                }
                }
            } catch(HeadlessException er)
        {
            System.out.println("ERROR: "+er.getMessage());
        } catch (SQLException ex) {               
           Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
       }               
                // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarMouseClicked

