    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
          try {
                if(txtidEmp.getText().equals("")){
                    JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la información",JOptionPane.INFORMATION_MESSAGE);
                }else{
                Statement sent=conexion.getStatement();
                if(sent==null)
                    return;
                String sentencia="DELETE FROM EMPLEO  WHERE IDEMPLEO='"+txtidEmp.getText()+"'";
                int estado=sent.executeUpdate(sentencia);
                if(estado==1)
                JOptionPane.showMessageDialog(null, "Empleo Eliminado","Operacion Exitosaexitosa",JOptionPane.INFORMATION_MESSAGE);
                }
                } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Empleo no Eliminado","Transaccion fallida",JOptionPane.ERROR);
            }
          
             txtVacantes.setEnabled(true);
           txtnombreEmp.setEnabled(true);
           txtDescrip.setEnabled(true);
           btnAceptar.setVisible(false);
           btnCancelar.setVisible(false);
           btnSalir.setVisible(true);
           btnAgregar.setVisible(true);
           btnEliminar.setVisible(true);
           lblEliminar.setVisible(false);    // TODO add your handling code here:
    }//GEN-LAST:event_btnAceptarMouseClicked

