    private void btAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAgregarMouseClicked
        try {
        if(txtNombre.getText().equals("")||txtidTaller.getText().equals("")||txtNombreTitular.getText().equals("")||txtNumAula.getText().equals("")||txtDescripcion.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la informaci√≥n",JOptionPane.INFORMATION_MESSAGE);
        }
        else{   
           
                DBConexion conexion=new DBConexion();
                Statement stent=conexion.getStatement();
                ResultSet rs=stent.executeQuery("SELECT * FROM TALLER ");
                th=new TallerHelper();
                tall=new Taller(txtidTaller.getText(),txtNombre.getText(), txtDescripcion.getText(), txtNomTitular.getText(), txtNumAula.getText());
                if(th.Agregar(tall)==true){
                    JOptionPane.showMessageDialog(null,"Operacion Exitosa","Taller Agregado",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(true);
                }
                }
            } catch(HeadlessException er)
        {
            System.out.println("ERROR: "+er.getMessage());
        } catch (SQLException ex) {               
           Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
       }               
        
        
    }//GEN-LAST:event_btAgregarMouseClicked

