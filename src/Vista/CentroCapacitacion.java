    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
       try {    if(txtidCenCap.getText().equals("")){
                    JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la información",JOptionPane.INFORMATION_MESSAGE);
                }else{
                Statement sent=conexion.getStatement();
                if(sent==null)
                    return;
                String sentencia="DELETE FROM CENTROCAPACITACION  WHERE IDCAPACITACION='"+txtidCenCap.getText()+"'";
                int estado=sent.executeUpdate(sentencia);
                if(estado==1)
                JOptionPane.showMessageDialog(null, "Centro de Capacitacion Eliminado","Operacion Exitosaexitosa",JOptionPane.INFORMATION_MESSAGE);
       }     
       } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Centro de Capacitacion no Eliminado","Transaccion fallida",JOptionPane.ERROR);
            } 
       lblElimarCentro.setVisible(false);
    txtCalle.setEnabled(true);
    txtColonia.setEnabled(true);
    txtDelegacion.setEnabled(true);
    txtNombre.setEnabled(true);
    txtTelefono.setEnabled(true);
    txtidCenCap.setEnabled(true);
    btnAceptar.setVisible(false);
    btnCancelar.setVisible(false);
    btnEliminar.setVisible(true);
    btnSalir.setVisible(true);
    btnAgregar.setVisible(true);
    btnAceptar.setVisible(false);
    }//GEN-LAST:event_btnAceptarMouseClicked

