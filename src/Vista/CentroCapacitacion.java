    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
             try {
        if(txtNombre.getText().equals("")||txtidCenCap.getText().equals("")||txtCalle.getText().equals("")||txtColonia.getText().equals("")||txtDelegacion.getText().equals("")||txtTelefono.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la información",JOptionPane.INFORMATION_MESSAGE);
        }
        else{   
           
                DBConexion conexion=new DBConexion();
                Statement stent=conexion.getStatement();
                ResultSet rs=stent.executeQuery("SELECT * FROM CENTROCAPACITACION ");
                capHelp=new CapacitacionHelper();
                cCap=new CentrosCapacitacion(txtidCenCap.getText(),txtNombre.getText(), txtCalle.getText(), txtColonia.getText(), txtDelegacion.getText(),txtTelefono.getText());
                if(capHelp.Agregar(cCap)==true){
                    JOptionPane.showMessageDialog(null,"Operacion Exitosa","Centro de Capacitacion Agregado",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(true);
                }
                }
            } catch(HeadlessException er)
        {
            System.out.println("ERROR: "+er.getMessage());
        } catch (SQLException ex) {               
           Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
       }               
        
        
        
    }//GEN-LAST:event_btnAgregarMouseClicked

