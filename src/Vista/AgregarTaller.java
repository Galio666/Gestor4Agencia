    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
         try {
                if(txtfolio.getText().equals("")||txtidTaller.getText().equals("")){
                    JOptionPane.showMessageDialog(null,"Datos no validos","Verificar la informaci√≥n",JOptionPane.INFORMATION_MESSAGE);
                }else{
                Statement sent=conexion.getStatement();
                if(sent==null)
                    return;
                String sentencia="UPDATE  PERFIL SET IDTALLER="+txtidTaller.getText()+"  WHERE FOLIO='"+txtfolio.getText()+"'";
                int estado=sent.executeUpdate(sentencia);
                if(estado==1)
                JOptionPane.showMessageDialog(null, "Usuario Agregado a Taller ","Operacion Exitosaexitosa",JOptionPane.INFORMATION_MESSAGE);
                }
                } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Usuario no Agregado a Taller","Transaccion fallida",JOptionPane.ERROR);
            }
                  // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarMouseClicked

